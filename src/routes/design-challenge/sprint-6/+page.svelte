    <div class="inner">
        <header>
            <h2 class="large-heading">Hoe werkt de design challenge van <br>Sprint 6: The Startup?</h2>
        </header>

        <div class="cta">
            <h3 class="small-heading">Werkwijze</h3>
            <ul>
                <li>Op basis van een bestaand idee, concept of ontwerp (bij voorkeur een desktop versie uitgewerkt in een figma bestand) gaan studenten FDND een responsive website of webpagina ontwerpen, maken en testen.</li>
                <li>Studenten werken individueel of in een team aan een project.</li>
                <li>Voor het project is er bestaande content zoals teksten, beeldmateriaal, video's, huisstijl of conceptmateriaal beschikbaar.</li>
                <li>De code voor het project wordt publiekelijk op GitHub bewaard en gepubliceerd.</li>
            </ul>

            <a href="https://github.com/fdnd-task/the-startup-responsive-interactieve-website/blob/main/docs/INSTRUCTIONS.md" class="button is-dark"><span>Bekijk de studentenopdracht</span></a>
        </div>

        <div class="cta">
            <h3 class="small-heading">Planning</h3>
            
            <ul>
                <li>Op de maandag in de <strong>1e week</strong> van de sprint krijgen de studenten <strong>een briefing</strong> bij de opdrachtgever. Tijdens de briefing maken de studenten kennis met het bedrijf en wordt besproken wat ze gaan maken.</li>
                <li>Op de donderdag van de <strong>3e week</strong> van de sprint wordt de opdracht bij de opdrachtgever <strong>gepresenteerd</strong> en <strong>opgeleverd</strong>.</li>
                <li>Tussentijdse contactmomenten zoals standups, vragenuurtje, communicatie via een teams channel zijn altijd mogelijk.</li>
            </ul>

            <a href="https://programma.fdnd.nl/static-web/the-startup#sprint-planning" class="button is-dark"><span>Bekijk de planning</span></a>
        </div>

        <div class="cta">
            <h3 class="small-heading">Voorbeelden van projecten</h3>
            <ul>
                <li>Teampagina voor een corporate website met foto's, namen, teksten en …</li>
                <li>Projectpagina voor een nieuw product met uitleg, beeldmateriaal, video, gebruikersreacties en …</li>
                <li>Contactpagina met informatie, routebeschrijving, een contactformulier en …</li>
                <li>Een nieuwe overzichtspagina met recente artikelen uit jullie blog …</li>
                <li>Zelf ideeën? Laten we sparren en een leuke opdracht verzinnen voor onze studenten!</li>
            </ul>

